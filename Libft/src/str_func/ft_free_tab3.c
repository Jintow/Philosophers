#include "libft.h"

char	***ft_free_tab3(char ***tab)
{
	int	i;

	if (!tab)
		return (NULL);
	i = 0;
	while (tab[i])
		ft_free_tab(tab[i++]);
	free(tab);
	return (NULL);
}
